/**
 * Feature vector for a board, used to measure distance from archetype centroid.
 * All values normalized to 0-1 range.
 */
export interface BoardFeatures {
  /** Highest card rank normalized: 0 (deuce) to 1 (ace) */
  highCardNorm: number;
  /** Wetness score from analyzeBoard() (already 0-1) */
  wetness: number;
  /** 1 if paired, 0 if not */
  isPaired: number;
  /** 0 = rainbow, 0.5 = two-tone, 1 = monotone */
  suitedness: number;
  /** 1 if has connectors, 0 if not */
  connectivity: number;
}

/**
 * Archetype centroid — the "ideal" board features for this archetype.
 * Used to measure how typical a specific board is.
 */
export interface ArchetypeCentroid {
  archetypeId: ArchetypeId;
  center: BoardFeatures;
  /** Per-feature tolerance (how much variation is normal) */
  tolerance: BoardFeatures;
}

/**
 * Convert a BoardTexture (from analyzeBoard()) to normalized features.
 */
export function boardToFeatures(texture: {
  highCard: number;
  wetness: number;
  isPaired: boolean;
  isMonotone: boolean;
  isTwoTone: boolean;
  isRainbow: boolean;
  hasConnectors: boolean;
}): BoardFeatures {
  return {
    highCardNorm: texture.highCard / 12, // 0=2, 12=A → 0-1
    wetness: texture.wetness,
    isPaired: texture.isPaired ? 1 : 0,
    suitedness: texture.isMonotone ? 1 : texture.isTwoTone ? 0.5 : 0,
    connectivity: texture.hasConnectors ? 1 : 0,
  };
}

