/** Standard GTO actions used in frequency tables */
export type GtoAction =
  | "fold"
  | "check"
  | "call"
  | "bet_small"    // ~33% pot
  | "bet_medium"   // ~66-75% pot
  | "bet_large"    // 100%+ pot (overbet)
  | "raise_small"
  | "raise_large";

/** Action frequencies for a single hand category: action → probability (0-1) */
export type ActionFrequencies = Partial<Record<GtoAction, number>>;

/** All hand category frequencies for one position */
export type PositionFrequencies = Partial<Record<HandCategory, ActionFrequencies>>;

export interface FrequencyTable {
  archetypeId: ArchetypeId;
  name: string;
  description: string;
  context: {
    street: "preflop" | "flop" | "turn" | "river";
    potType: "srp" | "3bet" | "4bet" | "bvb";
    heroPosition: string | string[];
    villainPosition: string | string[];
  };
  /** IP (in position) frequencies — hand category → action → frequency */
  ipFrequencies: PositionFrequencies;
  /** OOP (out of position) frequencies */
  oopFrequencies: PositionFrequencies;
  /** Available actions for IP */
  actionsIp: GtoAction[];
  /** Available actions for OOP */
  actionsOop: GtoAction[];
  /** Number of solver boards averaged (for postflop) */
  boardsAnalyzed?: number;
  /** One-line teaching principle */
  keyPrinciple: string;
  /** Common mistakes players make */
  commonMistakes: string[];
  /** Data provenance */
  source: string;
}

// ═══════════════════════════════════════════════════════
// FREQUENCY BANDS — variance across boards within an archetype
// ═══════════════════════════════════════════════════════

/**
 * Statistical distribution for a single action frequency across multiple boards.
 * Instead of a point estimate ("bet 55%"), shows the range ("bet 49-61%, avg 55%").
 *
 * The band width tells the user how board-specific the answer is:
 * - Narrow band (±3-5%): archetype approximation is very accurate
 * - Wide band (±15%+): board-specific details matter, answer is approximate
 */
export interface FrequencyBand {
  /** Average frequency across all solved boards */
  mean: number;
  /** Standard deviation — measures how tightly boards cluster */
  stdDev: number;
  /** Minimum frequency seen across any board */
  min: number;
  /** Maximum frequency seen across any board */
  max: number;
  /** How many boards contributed to this statistic */
  sampleCount: number;
}

/** Frequency bands for each action (replaces flat ActionFrequencies for band-aware tables) */
export type ActionFrequencyBands = Partial<Record<GtoAction, FrequencyBand>>;

/** Per-category band data for one position */
export type PositionFrequencyBands = Partial<Record<HandCategory, ActionFrequencyBands>>;

/**
 * Archetype accuracy metrics — how tightly the boards cluster.
 * Computed once during solver import, displayed in UI.
 */
export interface ArchetypeAccuracy {
  /** Average stdDev across all hand categories and actions (0-1, lower = tighter) */
  avgStdDev: number;
  /** Accuracy score: 1 - avgStdDev, clamped to [0, 1]. Higher = more accurate. */
  accuracy: number;
  /** Human label: "very high" (>0.95) | "high" (>0.90) | "moderate" (>0.80) | "approximate" */
  confidenceLabel: "very_high" | "high" | "moderate" | "approximate";
  /** Number of boards that contributed to this archetype */
  boardCount: number;
}

/**
 * Extended solver output format that includes per-board distributions.
 * Generated by the enhanced parse script (batch_solve.py parse --bands).
 */
export interface SolverOutputWithBands extends SolverOutput {
  /** Per-board frequency values (for computing bands in TypeScript) */
  ip_distributions?: Record<string, Record<string, number[]>>;
  oop_distributions?: Record<string, Record<string, number[]>>;
}

/**
 * Compute a FrequencyBand from an array of per-board frequency values.
 */
export function computeBand(values: number[]): FrequencyBand {
  if (values.length === 0) {
    return { mean: 0, stdDev: 0, min: 0, max: 0, sampleCount: 0 };
  }
  if (values.length === 1) {
    return { mean: values[0], stdDev: 0, min: values[0], max: values[0], sampleCount: 1 };
  }

  const n = values.length;
  const mean = values.reduce((a, b) => a + b, 0) / n;
  const variance = values.reduce((sum, v) => sum + (v - mean) ** 2, 0) / n;
  const stdDev = Math.sqrt(variance);
  const min = Math.min(...values);
  const max = Math.max(...values);

  return { mean, stdDev, min, max, sampleCount: n };
}

/**
 * Compute archetype accuracy from frequency bands.
 * Averages the stdDev across all categories and actions.
 */
export function computeArchetypeAccuracy(
  ipBands: PositionFrequencyBands,
  oopBands: PositionFrequencyBands,
  boardCount: number,
): ArchetypeAccuracy {
  const stdDevs: number[] = [];

  for (const bands of [ipBands, oopBands]) {
    for (const actionBands of Object.values(bands)) {
      if (!actionBands) continue;
      for (const band of Object.values(actionBands)) {
        if (band && band.sampleCount >= 2) {
          stdDevs.push(band.stdDev);
        }
      }
    }
  }

  const avgStdDev = stdDevs.length > 0
    ? stdDevs.reduce((a, b) => a + b, 0) / stdDevs.length
    : 0;

  const accuracy = Math.max(0, Math.min(1, 1 - avgStdDev));

  let confidenceLabel: ArchetypeAccuracy["confidenceLabel"];
  if (accuracy >= 0.95) confidenceLabel = "very_high";
  else if (accuracy >= 0.90) confidenceLabel = "high";
  else if (accuracy >= 0.80) confidenceLabel = "moderate";
  else confidenceLabel = "approximate";

  return { avgStdDev, accuracy, boardCount, confidenceLabel };
}

/**
 * Build band data from per-board distributions.
 */
export function buildPositionBands(
  distributions: Record<string, Record<string, number[]>>,
): PositionFrequencyBands {
  const result: PositionFrequencyBands = {};

  for (const [solverCat, actionDists] of Object.entries(distributions)) {
    const handCat = SOLVER_CATEGORY_MAP[solverCat];
    if (!handCat) continue;

    const bands: ActionFrequencyBands = {};
    for (const [action, values] of Object.entries(actionDists)) {
      const gtoAction = SOLVER_ACTION_MAP[action];
      if (gtoAction && values.length > 0) {
        bands[gtoAction] = computeBand(values);
      }
    }

    // Merge if category already exists (e.g. underpair + bottom_pair)
    if (result[handCat]) {
      const existing = result[handCat]!;
      for (const [action, band] of Object.entries(bands)) {
        const key = action as GtoAction;
        if (!existing[key]) {
          existing[key] = band;
        }
        // If both exist, we can't perfectly merge distributions — use weighted average
      }
    } else {
      result[handCat] = bands;
    }
  }

  return result;
}

/**
 * Convert SolverOutputWithBands to both a FrequencyTable and band data.
 */
export function solverOutputToTableWithBands(
  raw: SolverOutputWithBands,
  metadata: {
    name: string;
    description: string;
    keyPrinciple: string;
    commonMistakes: string[];
  },
): {
  table: FrequencyTable;
  ipBands: PositionFrequencyBands;
  oopBands: PositionFrequencyBands;
  accuracy: ArchetypeAccuracy;
} {
  const table = solverOutputToTable(raw, metadata);

  const ipBands = raw.ip_distributions
    ? buildPositionBands(raw.ip_distributions)
    : {};
  const oopBands = raw.oop_distributions
    ? buildPositionBands(raw.oop_distributions)
    : {};

  const accuracy = computeArchetypeAccuracy(ipBands, oopBands, raw.boardsAnalyzed);

  return { table, ipBands, oopBands, accuracy };
}

