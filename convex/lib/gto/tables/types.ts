/** Map solver hand categories to our HandCategory type */
const SOLVER_CATEGORY_MAP: Record<string, HandCategory> = {
  // Direct matches
  sets_plus: "sets_plus",
  two_pair: "two_pair",
  overpair: "overpair",
  top_pair_top_kicker: "top_pair_top_kicker",
  top_pair_weak_kicker: "top_pair_weak_kicker",
  middle_pair: "middle_pair",
  bottom_pair: "bottom_pair",
  flush_draw: "flush_draw",
  straight_draw: "straight_draw",
  combo_draw: "combo_draw",
  overcards: "overcards",
  air: "air",
  // Solver-specific mappings
  underpair: "bottom_pair",   // Underpair maps to bottom_pair category
};

/** Map solver action names to our GtoAction type */
const SOLVER_ACTION_MAP: Record<string, GtoAction> = {
  check: "check",
  fold: "fold",
  call: "call",
  bet_small: "bet_small",
  bet_medium: "bet_medium",
  bet_large: "bet_large",
};

/**
 * Convert raw solver output to our FrequencyTable format.
 * Merges solver categories into HandCategory (e.g. underpair → bottom_pair).
 */
export function solverOutputToTable(
  raw: SolverOutput,
  metadata: {
    name: string;
    description: string;
    keyPrinciple: string;
    commonMistakes: string[];
  },
): FrequencyTable {
  return {
    archetypeId: raw.archetypeId as ArchetypeId,
    name: metadata.name,
    description: metadata.description,
    context: {
      street: raw.context.street as FrequencyTable["context"]["street"],
      potType: raw.context.potType as FrequencyTable["context"]["potType"],
      heroPosition: raw.context.heroPosition,
      villainPosition: raw.context.villainPosition,
    },
    ipFrequencies: mapPositionFrequencies(raw.ip_frequencies),
    oopFrequencies: mapPositionFrequencies(raw.oop_frequencies),
    actionsIp: raw.actions_ip.map(mapAction).filter(Boolean) as GtoAction[],
    actionsOop: raw.actions_oop.map(mapAction).filter(Boolean) as GtoAction[],
    boardsAnalyzed: raw.boardsAnalyzed,
    keyPrinciple: metadata.keyPrinciple,
    commonMistakes: metadata.commonMistakes,
    source: `TexasSolver (${raw.boardsAnalyzed} boards)`,
  };
}

function mapPositionFrequencies(
  raw: Record<string, Record<string, number>>,
): PositionFrequencies {
  const result: PositionFrequencies = {};

  for (const [solverCat, freqs] of Object.entries(raw)) {
    const handCat = SOLVER_CATEGORY_MAP[solverCat];
    if (!handCat) continue;

    const mapped: ActionFrequencies = {};
    for (const [action, prob] of Object.entries(freqs)) {
      const gtoAction = SOLVER_ACTION_MAP[action];
      if (gtoAction && prob > 0.0001) {
        mapped[gtoAction] = prob;
      }
    }

    // Merge if category already exists (e.g. underpair + bottom_pair)
    if (result[handCat]) {
      const existing = result[handCat]!;
      for (const [action, prob] of Object.entries(mapped)) {
        const key = action as GtoAction;
        existing[key] = ((existing[key] ?? 0) + (prob ?? 0)) / 2;
      }
    } else {
      result[handCat] = mapped;
    }
  }

  return result;
}

function mapAction(solverAction: string): GtoAction | null {
  return SOLVER_ACTION_MAP[solverAction] ?? null;
}
