/**
 * Frequency table types — the data structure for GTO lookup tables.
 *
 * Each archetype has one FrequencyTable containing action frequencies
 * per hand category, for both IP and OOP positions.
 *
 * Pure TypeScript, zero Convex imports.
 */
import type { ArchetypeId } from "../archetypeClassifier";
import type { HandCategory } from "../handCategorizer";

// ═══════════════════════════════════════════════════════
// CORE TYPES
