/**
 * Score how typical a board is within its archetype (0-1).
 * 1.0 = perfectly typical (right at centroid), 0.0 = very atypical.
 *
 * Uses weighted Euclidean distance from the archetype centroid,
 * scaled by per-feature tolerance.
 */
export function scoreBoardTypicality(
  archetypeId: ArchetypeId,
  boardFeatures: BoardFeatures,
): number {
  const centroid = ARCHETYPE_CENTROIDS[archetypeId];
  if (!centroid) return 0.5; // Unknown archetype — assume moderate

  const { center, tolerance } = centroid;
  const keys: (keyof BoardFeatures)[] = [
    "highCardNorm", "wetness", "isPaired", "suitedness", "connectivity",
  ];

  let totalDist = 0;
  for (const key of keys) {
    const diff = Math.abs(boardFeatures[key] - center[key]);
    const tol = Math.max(tolerance[key], 0.01); // avoid div by zero
    const normalizedDiff = diff / tol;
    totalDist += normalizedDiff ** 2;
  }

  // RMS distance, then convert to 0-1 score (Gaussian falloff)
  const rmsDist = Math.sqrt(totalDist / keys.length);
  return Math.exp(-0.5 * rmsDist ** 2);
}

/**
 * Practical impact assessment — translates abstract accuracy into
 * what it actually means for the user's poker decisions and EV.
 */
export interface AccuracyImpact {
  /** Estimated accuracy 0-1 */
  accuracy: number;
  /** Human label: "Very High" | "High" | "Moderate" | "Approximate" */
  label: string;
  /** Detailed description for the user */
  description: string;
  /** Estimated max EV impact in BB per decision from the accuracy gap */
  maxEvImpactBB: number;
  /** Whether the accuracy gap could change the "optimal" action */
  couldFlipOptimal: boolean;
  /** Plain-language "what this means" for a poker player */
  practicalMeaning: string;
}

/**
 * Estimate how accurate the archetype frequency is for a specific board,
 * AND what that accuracy gap means in practical poker terms.
 *
 * The key insight: accuracy gaps matter very differently depending on
 * how close the top two actions are in frequency.
 *
 * - Clear spots (one action dominates 60%+): accuracy gap is invisible.
 *   Even a 10% shift doesn't change the user's decision.
 * - Close spots (top actions within 10% of each other): accuracy gap
 *   could flip which action appears "optimal," but both are GTO-correct
 *   because the solver MIXES between them.
 *
 * @param archetypeAccuracy - Archetype-level accuracy metrics
 * @param boardTypicality - How typical this board is (0-1, from scoreBoardTypicality)
 * @param potSizeBB - Current pot size in big blinds (for EV impact calculation)
 * @param topActionGap - Gap between the two highest-frequency actions (0-1).
 *   If undefined, uses a conservative estimate from the accuracy data.
 */
export function estimateBoardAccuracy(
  archetypeAccuracy: ArchetypeAccuracy,
  boardTypicality: number,
  potSizeBB?: number,
  topActionGap?: number,
): AccuracyImpact {
  // Board accuracy = archetype accuracy × typicality weighting
  // Typical boards get full archetype accuracy; atypical boards get discounted
  const accuracy = archetypeAccuracy.accuracy * (0.5 + 0.5 * boardTypicality);
  const clamped = Math.max(0, Math.min(1, accuracy));
  const pct = Math.round(clamped * 100);
  const gap = 1 - clamped; // e.g. 0.04 for 96% accuracy

  // ── EV impact calculation ──
  // The accuracy gap (stdDev) tells us how much the frequency could shift.
  // EV impact = frequency_shift × pot × decision_sensitivity
  // Decision sensitivity depends on how close the top actions are:
  // - If one action is clearly best (60%+ frequency), shifting by 4% changes nothing
  // - If two actions are 50/50, shifting by 4% could matter but EV diff is tiny
  const pot = potSizeBB ?? 10; // default 10BB pot
  const frequencyUncertainty = archetypeAccuracy.avgStdDev;

  // Maximum EV impact: if you pick the wrong action due to the gap,
  // you lose roughly (stdDev × pot × 0.5). The 0.5 factor accounts for
  // the fact that when frequencies are close, EV between actions is also close.
  const maxEvImpactBB = Math.round(frequencyUncertainty * pot * 0.5 * 100) / 100;

  // Could the gap flip the optimal action?
  // Only if the top-action gap is smaller than 2× the frequency uncertainty
  const effectiveGap = topActionGap ?? 0.15; // conservative: assume moderate gap
  const couldFlipOptimal = effectiveGap < frequencyUncertainty * 2;

  // ── Labels and descriptions ──
  let label: string;
  let description: string;
  let practicalMeaning: string;

  if (pct >= 95) {
    label = "Very High";
    description = `Solver analysis of ${archetypeAccuracy.boardCount} similar boards shows very consistent strategy. This board is highly typical — estimated accuracy ~${pct}%.`;
    practicalMeaning = maxEvImpactBB < 0.1
      ? "The accuracy gap has zero practical impact on your decisions. Play exactly as shown."
      : `At most ~${maxEvImpactBB} BB per decision in a ${pot} BB pot. The recommended action is reliable.`;
  } else if (pct >= 88) {
    label = "High";
    description = `Based on ${archetypeAccuracy.boardCount} solver runs. This board fits the pattern well — estimated accuracy ~${pct}%.`;
    practicalMeaning = couldFlipOptimal
      ? `Close spot — the top actions are similar in EV. Both are correct; the "best" one might differ by ~${maxEvImpactBB} BB in a ${pot} BB pot.`
      : `Clear spot — the recommended action is right. The gap costs at most ~${maxEvImpactBB} BB in a ${pot} BB pot.`;
  } else if (pct >= 75) {
    label = "Moderate";
    description = `Frequencies vary across similar boards. This board has some atypical features — estimated accuracy ~${pct}%. Board-specific details may shift frequencies.`;
    practicalMeaning = `Frequencies could shift ±${Math.round(frequencyUncertainty * 100)}% on the exact board. In a ${pot} BB pot, that's up to ~${maxEvImpactBB} BB. Use the direction, not the exact numbers.`;
  } else {
    label = "Approximate";
    description = `This board is at the edge of the pattern — estimated accuracy ~${pct}%. The exact solver answer may differ meaningfully. Treat frequencies as directional guidance.`;
    practicalMeaning = `This spot varies significantly across boards in this category. In a ${pot} BB pot, the EV gap could be ~${maxEvImpactBB} BB. Focus on the general principle rather than exact frequencies.`;
  }

  return {
    accuracy: clamped,
    label,
    description,
    maxEvImpactBB,
    couldFlipOptimal,
    practicalMeaning,
  };
}

/**
 * Compute the gap between the top two actions in a frequency distribution.
 * Used as input to estimateBoardAccuracy for precise impact assessment.
 *
 * @returns Gap between the highest and second-highest frequency (0-1).
 *   Returns 1.0 if only one action exists.
 */
export function computeTopActionGap(frequencies: ActionFrequencies): number {
  const values = Object.values(frequencies)
    .filter((v): v is number => v !== undefined && v > 0.001)
    .sort((a, b) => b - a);

  if (values.length <= 1) return 1.0;
  return values[0] - values[1];
}

// ═══════════════════════════════════════════════════════
// SAMPLE SIZE ANALYSIS — how many more boards do we need?
// ═══════════════════════════════════════════════════════

/**
 * Projection of what more solver boards would buy for an archetype.
 */
export interface SampleSizeProjection {
  /** Current number of boards */
  currentBoards: number;
  /** Current average standard error across all actions */
  currentStdError: number;
  /** Current max EV impact (BB) at a reference pot size */
  currentMaxEvBB: number;
  /** Projections at various board counts */
  projections: {
    boards: number;
    stdError: number;
    maxEvBB: number;
    /** Improvement vs current (0-1, e.g. 0.30 = 30% tighter) */
    improvement: number;
    /** Marginal improvement per additional board (diminishing returns indicator) */
    marginalGainPerBoard: number;
  }[];
  /** Recommended sweet spot: board count where marginal returns drop below threshold */
  sweetSpot: number;
  /** Whether current data is already "good enough" (stdError < 0.01) */
  sufficientlyPrecise: boolean;
  /** Human summary */
  summary: string;
}

/**
 * Analyze how many more boards an archetype needs to reach a target precision.
 *
 * Uses the statistical relationship: stdError = stdDev / √n
 * More boards reduce the standard error of the mean (how confident we are
 * in the average frequency), but with diminishing returns (√n).
 *
 * @param currentBands - Current frequency bands from solver output
 * @param currentBoards - How many boards are currently solved
 * @param refPotBB - Reference pot size for EV impact (default 10 BB)
 * @returns Projection with sweet spot recommendation
 */
export function analyzeSampleSize(
  currentBands: PositionFrequencyBands,
  currentBoards: number,
  refPotBB: number = 10,
): SampleSizeProjection {
  // Collect all stdDevs from current band data
  const stdDevs: number[] = [];
  for (const actionBands of Object.values(currentBands)) {
    if (!actionBands) continue;
    for (const band of Object.values(actionBands)) {
      if (band && band.sampleCount >= 2) {
        stdDevs.push(band.stdDev);
      }
    }
  }

  if (stdDevs.length === 0) {
    return {
      currentBoards,
      currentStdError: 0,
      currentMaxEvBB: 0,
      projections: [],
      sweetSpot: currentBoards,
      sufficientlyPrecise: true,
      summary: "No variance data available — cannot project sample size needs.",
    };
  }

  // Average population stdDev (this is inherent to the archetype, not reducible)
  const avgPopStdDev = stdDevs.reduce((a, b) => a + b, 0) / stdDevs.length;
  // Max population stdDev (worst-case action)
  const maxPopStdDev = Math.max(...stdDevs);

  // Current standard error of the mean
  const currentStdError = avgPopStdDev / Math.sqrt(currentBoards);
  const currentMaxEvBB = Math.round(currentStdError * refPotBB * 0.5 * 100) / 100;

  // Project forward
  const targetBoards = [25, 40, 50, 75, 100, 150, 200, 300, 500];
  const projections = targetBoards
    .filter(n => n > currentBoards) // only show future projections
    .map(boards => {
      const stdError = avgPopStdDev / Math.sqrt(boards);
      const maxEvBB = Math.round(stdError * refPotBB * 0.5 * 100) / 100;
      const improvement = 1 - stdError / currentStdError;

      // Marginal gain: improvement per additional board from previous step
      const prevBoards = boards === targetBoards.filter(n => n > currentBoards)[0]
        ? currentBoards
        : targetBoards.filter(n => n > currentBoards && n < boards).pop() ?? currentBoards;
      const prevStdError = avgPopStdDev / Math.sqrt(prevBoards);
      const marginalGainPerBoard = (prevStdError - stdError) / (boards - prevBoards);

      return { boards, stdError, maxEvBB, improvement, marginalGainPerBoard };
    });

  // Find sweet spot: where marginal gain per board drops below 0.0001
  // (i.e., each additional board improves stdError by less than 0.01%)
  const MARGINAL_THRESHOLD = 0.00005;
  let sweetSpot = currentBoards;
  for (const p of projections) {
    if (p.marginalGainPerBoard > MARGINAL_THRESHOLD) {
      sweetSpot = p.boards;
    } else {
      break;
    }
  }
  // If no projection crosses the threshold, the smallest projection is sufficient
  if (sweetSpot === currentBoards && projections.length > 0) {
    sweetSpot = projections[0].boards;
  }

  const sufficientlyPrecise = currentStdError < 0.01; // < 1% standard error

  // Build summary
  let summary: string;
  if (sufficientlyPrecise) {
    summary = `Already precise at ${currentBoards} boards (std error ${(currentStdError * 100).toFixed(1)}%). More boards would have negligible impact. The frequencies shown are reliable to ±${(currentStdError * 100).toFixed(1)}%.`;
  } else {
    const sweetProj = projections.find(p => p.boards === sweetSpot);
    const sweetEv = sweetProj?.maxEvBB ?? currentMaxEvBB;
    const additionalNeeded = sweetSpot - currentBoards;
    summary = `Current: ${currentBoards} boards, ±${(currentStdError * 100).toFixed(1)}% uncertainty (~${currentMaxEvBB} BB EV impact in ${refPotBB} BB pot). Sweet spot: ${sweetSpot} boards (+${additionalNeeded} more) would narrow to ±${((avgPopStdDev / Math.sqrt(sweetSpot)) * 100).toFixed(1)}% (~${sweetEv} BB). Beyond ${sweetSpot} boards: diminishing returns.`;
  }

  return {
    currentBoards,
    currentStdError,
    currentMaxEvBB,
    projections,
    sweetSpot,
    sufficientlyPrecise,
    summary,
  };
}

/**
 * Quick check: for a given population stdDev and current n,
 * how many total boards to reach a target stdError?
 *
 * Formula: n = (stdDev / targetStdError)²
 */
export function boardsNeededForPrecision(
  populationStdDev: number,
  targetStdError: number,
): number {
  if (targetStdError <= 0) return Infinity;
  return Math.ceil((populationStdDev / targetStdError) ** 2);
}

